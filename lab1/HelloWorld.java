public class HelloWorld {
	public static void main(String[] args) {
		System.out.println("hello world");
	}
}

/*
1. All code in Java must be part of a class. 必须从属于一个类
2. We delimit the beginning and end of segments of code
   using { and }. 必须用{}表示内容
3. All statements in Java must end in a semi-colon. 结尾要有分号
4. For code to run we need public static void main(String[] args) 需要固定格式（后面会详细介绍）
*/